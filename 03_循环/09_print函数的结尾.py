# 在默认情况下，print 函数输出内容之后，会自动在内容末尾增加换行
print("*")  # 自动换行
print(".............")
print("*", end="\n")  # 默认结束符,换行
print(".............")

print("*", end="--|--|--")  # 修改结束符
print(".............")

print("*", end="\t")  # 列对齐
print(".............")

print("*", end="")  # 取消自动换行
print(".............")
