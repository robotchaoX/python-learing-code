# + 加
num1 = 10 + 20
# 30

# - 减
num2 = 10 - 20
# -10

# * 乘
num3 = 10 * 20
# 200
# *字符串,字符串重复指定次数
print("-" * 50)

#! / 除法 (实数除法,c++是整除)
num4 = 10 / 20
print(num4)  # 0.5

#! // 整除 (取除法的整数部分）
num5 = 9 // 2
print(num5)  # 4

#! % 求余
num6 = 9 % 2
print(num6)  # 1

#! ** 幂又称乘方
num7 = 2 ** 3
print(num7)  # 8
