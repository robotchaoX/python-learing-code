# 列表
# 列表里数据的类型可以不同
list1 = ["zhangsan", 18, 1.75, True]  # 下标从0开始
print(type(list1))
print(list1)


# 多个数据列表
li1 = [10, 20, 30]
print(type(li1))

# 单个数据列表
li2 = [10]  # 单个数据列表
print(type(li2))  # list
print(li2)
# 单个数据列表,不需要像tuple一样末尾加逗号，标识
li3 = [10, ]  # 末尾逗号，有没有都行
print(type(li3))  # list
print(li3)
 